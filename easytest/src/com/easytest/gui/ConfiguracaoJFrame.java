    private void projetosJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_projetosJTableMouseClicked

        int rowSelect = projetosJTable.getSelectedRow();

        if (rowSelect >= 0) {
            Integer codigo = (Integer) projetosJTable.getModel().getValueAt(rowSelect, 0);
            try {
                Projeto p = dao.getProjeto(codigo);

                if (p == null) {
                    throw new ReportTestException("O projeto selecionado não foi encontrado!");
                }
                setProjeto(p);
            } catch (ReportTestException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), this.getTitle(), JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_projetosJTableMouseClicked
