    //limpa os campos da tela
    public void clearForm() {
        nomeProjetoJTextField.setText("");
        reportsJTextField.setText("");
        descricaoJTextArea.setText("");
        iProjeto = null;
        ativoJCheckBox.setSelected(false);
    }

    public void setProjeto(Projeto p) {
        nomeProjetoJTextField.setText(p.getNome());
        reportsJTextField.setText(p.getReport());
        descricaoJTextArea.setText(p.getDescricao());
        iProjeto = p.getIProjeto();

        //verifica se é projeto principal
        if (p.getAtivo() != null && "S".equals(p.getAtivo())) {
            ativoJCheckBox.setSelected(true);
        } else {
            ativoJCheckBox.setSelected(false);
        }
    }

    public Projeto getProjeto() {
        Projeto p = new Projeto();
        p.setIProjeto(iProjeto);
        p.setNome(nomeProjetoJTextField.getText());
        p.setDescricao(descricaoJTextArea.getText());
        p.setReport(reportsJTextField.getText());
        if (ativoJCheckBox.isSelected()) {
            p.setAtivo("S");
        } else {
            p.setAtivo(null);
        }

        return p;
    }

    public final void initValues() {
        clearForm();

        //busca todos os projetos do banco de dados e adiciona no JTable
        try {
            List<Projeto> pList = dao.getAllProjeto();
            Object[][] dadosTabela = new Object[pList.size()][3];
            for (int i = 0; i < pList.size(); i++) {
                Projeto p = pList.get(i);
                dadosTabela[i][0] = p.getIProjeto();
                dadosTabela[i][1] = p.getNome();

                //verifica se é projeto principal
                if (p.getAtivo() != null && "S".equals(p.getAtivo())) {
                    dadosTabela[i][2] = "Sim";

                    //seta o projeto corrente
                    DataManager.getInstance().setProjeto(p);
                } else {
                    dadosTabela[i][2] = "Não";
                }
            }

            //passa os dados para a tabela
            projetosJTable.setModel(new javax.swing.table.DefaultTableModel(
                    dadosTabela,
                    new String[]{
                        "Codigo", "Nome", "Ativo"
                    }));


        } catch (ReportTestException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), this.getTitle(), JOptionPane.ERROR_MESSAGE);
        }
    }

